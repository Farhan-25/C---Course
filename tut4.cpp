#include<iostream>
using namespace std;

int main(){
    int a = 14 , b = 15;
    float pi=3.14;
    char c='u';
    bool is_true=true;
    bool is_false=false;

    cout<<"This is tutorial 4\n";
    cout<<"The value of a is: "<< a << "\n";
    cout<<"The value of b is: "<< b << "\n";
    cout<<"The value of pi is: "<< pi << "\n";
    cout<<"The value of c is: "<< c << "\n";
    cout<<"The value of is_true is: "<< is_true << "\n";                // Value of True is 1 in Boolean
    cout<<"The value of is_false is: "<< is_false << "\n";              // Value of False is 0 in Boolean
    return 0;
}

